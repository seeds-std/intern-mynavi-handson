<?php

/* -------------------------------------------------------------------------------- *
 * このファイルは[配列]と[配列のループ処理 (foreach)]の問題です
 *
 * 以下の処理をそれぞれ作成してください
 * -------------------------------------------------------------------------------- */

$fruits = [ 'apple', 'orange' ];

/* -------------------------------------------------------------------------------- *
 * $fruits 配列から "orange" を出力する処理を書いてください
 * -------------------------------------------------------------------------------- */
echo $fruits[1] . '<br>';

/* -------------------------------------------------------------------------------- *
 * $fruits 配列へ何かを追加する処理を書いてください
 * -------------------------------------------------------------------------------- */
$fruits[] = 'kiwifruit';

/* -------------------------------------------------------------------------------- *
 * $fruits 配列の "apple" を "pineapple" へ上書きする処理を書いてください
 * -------------------------------------------------------------------------------- */
$fruits[0] = 'pineapple';

/* -------------------------------------------------------------------------------- *
 * $fruits 配列の中身を全て出力する処理を作成してください
 * ここまで全てできていれば以下が出力されるかと思います
 * - pineapple
 * - orange
 * - 追加した値
 * -------------------------------------------------------------------------------- */
echo '<br>';
foreach ($fruits as $fruit) {
    echo $fruit . '<br>';
}
