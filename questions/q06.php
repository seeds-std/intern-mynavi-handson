<?php

/* -------------------------------------------------------------------------------- *
 * このファイルは[配列のループ処理 (foreach)]の問題です
 *
 * $numbers 配列の中から偶数のみを取り出し $even に格納するプログラムを作成してください
 * -------------------------------------------------------------------------------- */

$numbers = [ 5, 6, 25, 26, 35, 67, 68, 72, 82, 85 ];
$even = [];

foreach ($numbers as $number) {
    if ($number % 2 === 0) {
        $even[] = $number;
    }
}
